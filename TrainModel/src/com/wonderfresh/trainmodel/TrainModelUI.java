    public void on(int attribute){
        //1 = ac, 2 = heat, 3 = lights, 4 = left doors, 5 = right doors, 6 = service brakes
        if(attribute == 1){
            acOnLabel.setForeground(Color.black);
            acOffLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 2){
            heatOnLabel.setForeground(Color.black);
            heatOffLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 3){
            lightsOnLabel.setForeground(Color.black);
            lightsOffLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 4){
            leftDoorsOpenLabel.setForeground(Color.black);
            doorsClosedLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 5){
            rightDoorsOpenLabel.setForeground(Color.black);
            doorsClosedLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else{
            serviceBrakesOnLabel.setForeground(Color.black);
            serviceBrakesOffLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
    }
    public void off(int attribute){
        //1 = ac, 2 = heat, 3 = lights, 4 = left doors, 5 = right doors, 6 = service brakes
        if(attribute == 1){
            acOffLabel.setForeground(Color.black);
            acOnLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 2){
            heatOffLabel.setForeground(Color.black);
            heatOnLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 3){
            lightsOffLabel.setForeground(Color.black);
            lightsOnLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 4){
            if(rightDoorsOpenLabel.getForeground() != Color.black)
                doorsClosedLabel.setForeground(Color.black);
            leftDoorsOpenLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else if(attribute == 5){
            if(leftDoorsOpenLabel.getForeground() != Color.black)
                doorsClosedLabel.setForeground(Color.black);
            rightDoorsOpenLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
        else{
            serviceBrakesOffLabel.setForeground(Color.black);
            serviceBrakesOnLabel.setForeground(new java.awt.Color(120, 120, 120));
        }
    }
    public void fail(int attribute){
        //1 = ac, 2 = heat, 3 = lights, 4 = left doors, 5 = right doors, 6 = service brakes
        if(attribute == 1){
            acLabel.setForeground(new java.awt.Color(120, 120, 120));
            acOffLabel.setForeground(new java.awt.Color(120, 120, 120));
            acOnLabel.setForeground(new java.awt.Color(120, 120, 120));
            powerFailureLabel.setForeground(Color.red);
        }
        else if(attribute == 2){
            heatLabel.setForeground(new java.awt.Color(120, 120, 120));
            heatOffLabel.setForeground(new java.awt.Color(120, 120, 120));
            heatOnLabel.setForeground(new java.awt.Color(120, 120, 120));
            powerFailureLabel.setForeground(Color.red);
        }
        else if(attribute == 3){
            lightsLabel.setForeground(new java.awt.Color(120, 120, 120));
            lightsOffLabel.setForeground(new java.awt.Color(120, 120, 120));
            lightsOnLabel.setForeground(new java.awt.Color(120, 120, 120));
            powerFailureLabel.setForeground(Color.red);
        }
        else if(attribute == 4){
            doorsLabel.setForeground(new java.awt.Color(120, 120, 120));
            leftDoorsOpenLabel.setForeground(new java.awt.Color(120, 120, 120));
            powerFailureLabel.setForeground(Color.red);
        }
        else if(attribute == 5){
            doorsLabel.setForeground(new java.awt.Color(120, 120, 120));
            rightDoorsOpenLabel.setForeground(new java.awt.Color(120, 120, 120));
            powerFailureLabel.setForeground(Color.red);
        }
        else{
            serviceBrakeLabel.setForeground(new java.awt.Color(120, 120, 120));
            serviceBrakesOffLabel.setForeground(new java.awt.Color(120, 120, 120));
            serviceBrakesOnLabel.setForeground(new java.awt.Color(120, 120, 120));
            brakeFailureLabel.setForeground(Color.red);
        }
    }
    
